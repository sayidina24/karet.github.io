                <p>Membran karet, atau yang sering disebut <em>diaphragm</em>, adalah komponen esensial dalam berbagai sistem dan mesin. Fungsi utamanya adalah meneruskan tekanan yang terjadi pada salah satu bilik (<em>room</em>) ke bilik yang lain. Dengan kemampuan ini, membran dapat diaplikasikan secara luas sebagai bagian penting pada <em>valve</em> (katup), <em>sensor</em>, <em>blower</em>, hingga sistem rem dan kompresor.</p>
