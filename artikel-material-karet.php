        <h1 style="color: #336699; margin-bottom: 10px;">Mengenal Lebih Dalam Material Karet: Alam vs. Sintetis</h1>
        <p style="font-size: 0.9em; color: #777; margin-top: 0;">Diposting pada: 30 Mei 2025 | Kategori: Material Karet</p>

        <hr style="border-color: #EEE; margin: 20px 0;">

        <p>Karet, dengan sifat elastisitasnya yang unik, telah menjadi material tak tergantikan dalam berbagai aspek kehidupan dan industri. Namun, tahukah Anda bahwa ada dua jenis utama karet yang digunakan, yaitu karet alam dan karet sintetis? Keduanya memiliki asal, karakteristik, dan aplikasi yang berbeda.</p>

        <h2>Karet Alam: Hadiah dari Alam</h2>
        <p>Karet alam berasal dari lateks, cairan putih susu yang diekstrak dari pohon karet (Hevea brasiliensis). Proses pengumpulan lateks yang dikenal dengan 'penyadapan' telah dilakukan selama berabad-abad dan merupakan contoh bagaimana alam menyediakan material serbaguna bagi kita.</p>

        <div style="text-align: center; margin: 20px 0;">
            <img src="img/kebun_karet.jpg" alt="Kebun Karet" style="max-width: 80%; height: auto; border: 1px solid #ccc; box-shadow: 2px 2px 5px #888;">
            <p style="font-size: 0.8em; color: #777; margin-top: 5px;">Kebun karet yang menjadi sumber utama karet alam.</p>
        </div>

        <p>Sifat utama karet alam meliputi elastisitas yang sangat baik, ketahanan sobek yang tinggi, dan kemampuan yang baik dalam menghadapi tekanan dinamis. Material ini sering digunakan pada ban kendaraan, sarung tangan medis, dan berbagai produk yang membutuhkan kelenturan tinggi.</p>

        <h2>Karet Sintetis: Inovasi Buatan Manusia</h2>
        <p>Berbeda dengan karet alam, karet sintetis adalah polimer buatan yang diproduksi melalui proses kimia dari produk sampingan minyak bumi atau hidrokarbon lainnya. Perkembangan karet sintetis dimulai pada awal abad ke-20 sebagai respons terhadap kebutuhan akan material dengan sifat spesifik yang tidak dimiliki karet alam atau untuk mengatasi keterbatasan pasokan.</p>

        <div style="text-align: center; margin: 20px 0;">
            <img src="img/image_415695.png" alt="Proses Produksi Karet Sintetis" style="max-width: 80%; height: auto; border: 1px solid #ccc; box-shadow: 2px 2px 5px #888;">
            <p style="font-size: 0.8em; color: #777; margin-top: 5px;">Karet sintetis diproduksi melalui proses kimia di pabrik.</p>
        </div>

        <p>Ada banyak jenis karet sintetis, masing-masing dengan karakteristik unik yang disesuaikan untuk aplikasi tertentu:</p>
        <ul>
            <li>**NBR (Nitrile Butadiene Rubber):** Tahan terhadap minyak, bahan bakar, dan bahan kimia. Ideal untuk seal, O-ring, dan selang.</li>
            <li>**EPDM (Ethylene Propylene Diene Monomer):** Sangat baik dalam ketahanan terhadap cuaca, ozon, dan panas. Sering digunakan untuk segel jendela, seal otomotif, dan waterproofing.</li>
            <li>**Silicone:** Tahan suhu ekstrem (baik panas maupun dingin) dan inert secara kimia. Umum untuk aplikasi medis, makanan, dan isolator listrik.</li>
            <li>**Viton (Fluoroelastomer):** Ketahanan superior terhadap suhu tinggi, bahan kimia korosif, dan minyak. Digunakan di industri dirgantara dan kimia.</li>
            <li>**SBR (Styrene Butadiene Rubber):** Mirip dengan karet alam dalam beberapa aspek, sering digunakan untuk ban dan produk umum lainnya.</li>
        </ul>

