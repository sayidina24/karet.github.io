        <p>Karet sintetis, atau polimer, merupakan jenis elastomer buatan yang dihasilkan melalui di-sintesis dari produk sampingan minyak bumi. Elastomer sendiri adalah bahan dengan mekanik (materi) properti yang dapat mengalami de-formasi (pembentukan kembali) jauh lebih elastis di bawah tekanan dari sebagian besar bahan dan masih bisa kembali ke ukuran sebelumnya tanpa de-formasi permanen. Sekitar 15 miliar kilogram karet diproduksi setiap tahunnya, dan dari jumlah itu dua per tiga adalah produk sintetik. Karet sintetis, seperti juga karet alam, memiliki kegunaan dalam berbagai industri mulai dari Industri Rumah Tangga sampai Industri Skala Besar, antara lain untuk profil pintu dan jendela, selang (hose), ikat pinggang, anyaman, lantai dan peredam.</p>

        <h2 style="color: #337ab7;">Karet Alam & Karet Sintetis</h2>
        <img src="img/kebun_karet.jpg" alt="Joint Sheet Natural Rubber, Neoprene Rubber Sheet" style="max-width: 100%; height: auto; display: block; margin: 20px auto;">
        <p>Karet alam, yang berasal dari lateks Hevea Brasiliensis, terutama poli-cis-isoprena mengandung jejak kotoran seperti protein, kotoran dll. Meskipun bersifat sangat baik dalam hal kinerja mekanik, namun karet alam sering kalah dengan karet sintetis tertentu, terutama yang berkaitan dengan stabilitas termal dan kompatibilitas dengan produk minyak bumi.</p>

        <h2 style="color: #337ab7;">Sejarah</h2>
        <p>Pada tahun 1879, Bouchardat menciptakan salah satu bentuk karet sintetis, yang disebut Polimer Isoprena. Perluasan penggunaan kendaraan bermotor, dan ban kendaraan bermotor khususnya, yang dimulai pada tahun 1890, menciptakan peningkatan akan permintaan karet. Pada tahun 1909, sebuah tim yang dipimpin oleh Fritz Hofmann, bekerja di laboratorium Bayer di Elberfeld, Jerman, juga berhasil mem-polimerisasi Metil Isoprena, karet sintetis pertama. Ilmuwan Rusia Sergei Vasiljevich Lebedev menciptakan polimer karet pertama disintesis dari Butadiena pada tahun 1910. Bentuk karet sintetis memberikan dasar untuk produksi komersial skala besar, yang terjadi selama Perang Dunia I sebagai akibat dari berkurangnya karet alam. Bentuk awal karet sintetis lagi-lagi diganti dengan karet alam setelah perang berakhir, namun penyelidikan mengenai karet sintetis terus berlanjut. Ivan Ostromislensky melakukan penelitian awal yang signifikan pada karet sintetis dan beberapa monomer pada awal abad 20.</p>
        <p>Masalah politik yang dihasilkan dari fluktuasi besar dalam biaya karet alam menyebabkan diberlakukannya UU Stevenson pada tahun 1921. Tindakan ini pada dasarnya menciptakan sebuah kartel yang mengatur produksi.</p>

